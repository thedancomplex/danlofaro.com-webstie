<h1>The Narrative Machine</h1>

 <p style="text-align:justify">
<b>The Narrative Machine</b> is the result of a unique recycling experiment -- a cross-disciplinary collaboration between the School of Art Associate Professor and new media artist, Edgar Endress and Dr. Daniel Lofaro from the Volgenau School of Engineering.  Their ongoing series of sound sculptures uses new media technology to give discarded musical instruments a new life as art.  In its final form <i>The Narrative Machine</i> will be a large installation with a series of single instruments activated by microprocessors which will play together as an automatic orchestra.  Using an open system, this project allows for diverse inputs from multiple data source, in effect transforming any given dataset into a musical narrative.

The instruments used for this project were contributed as a part of Potomac Arts Academy's "instruments in the "attic" program which puts unused musical instruments back in the hands of deserving students and community members.  Every year hundreds of instruments are donated and repaired, but some are beyond repair.  Instead of throwing them away, they are being upcycled by mason's School of Art and the Volgenau School of Engineering, with the support of the Patriot Green Fund and transformed into these innovative sound sculptures. 
